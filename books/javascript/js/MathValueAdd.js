// ------------------------------------------------------------
function MathValueAdd ( value_1 , value_2 ) {
    // 第01引数と第02引数で渡された数値を合計し、結果を返す
    return value_1 + value_2;
}

// ------------------------------------------------------------
// MathValueAdd という関数を実行
//（ 第01引数に 3、第02引数に 12 を指定 )
// ------------------------------------------------------------
var total = MathValueAdd( 3 , 12 );

// 出力テスト
console.log(total); // 15
document.write(total); // 15